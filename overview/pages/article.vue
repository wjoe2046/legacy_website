            <a
              href="https://www.statista.com/statistics/201183/forecast-of-smartphone-penetration-in-the-us/"
              >over 70%</a
            >
            of the entire US population estimated to own one. Bluetooth is a radio
            protocol that can be used to wirelessly communicate between nearby
            mobile devices and the signal strength can be used to estimate
            distance.
          </p>
          <p>
            Mobile devices can be made to proactively record contact events with
            other nearby devices by sending Bluetooth signals. By measuring the
            signal strength and discrete number of contact events, the duration
            and distance of contact between two phone users can be estimated.
          </p>
          <p>
            By recording all contact events, a high accuracy list of at-risk
            individuals can be generated automatically when a new person is
            diagnosed. These individuals can then be immediately notified to
            ensure they self-isolate before infecting more people.
          </p>
          <p>
            <strong>
              Bluetooth proximity may be the most accurate crowdsourcing method
              for approximating close contact.
            </strong>
          </p>
          <p>
            While GPS data is a more well-known general technology, there are
            significant advantages bluetooth has over GPS in terms of accuracy for
            contact tracing. With bluetooth, proximity can be approximated by
            signal strength that is reduced by obstructions like walls; therefore,
            it more accurately reflects functional proximity in high-risk
            environments for close contact: inside buildings, in vehicles and
            airplanes, and in underground transit.
          </p>
          <p>
            <strong>
              Bluetooth communication also occurs directly between mobile devices.
              This means a decentralized system can be built more easily with and
              with stronger privacy protection than other crowdsourcing data types
              like GPS trajectories.
            </strong>
          </p>
          <p>
            We are pursuing research in developing inexpensive
            <a
              href="https://www.amazon.com/DSD-TECH-Bluetooth-CC2640R2F-Arduino/dp/B07MBLVHH8"
              >external Bluetooth devices</a
            >
            under the same automatic contact tracing alert system for use in
            countries with fewer smartphone users. These methods would face much
            steeper adoption challenges, but if mobile app users and external
            device users could be integrated under a single system, outcomes could
            be further improved over more global communities.
          </p>

