        <p><em>What challenges are you facing?</em></p>

        <p>
          The biggest specific challenge we&rsquo;re working on right now is
          navigating always-on Bluetooth. Most mobile operating systems
          discourage background apps using Bluetooth, as it tends to drain the
          battery. Firstly, we have to work around this discouragement;
          unfortunately, the status quo is the result of an arms race between
          apps and the mobile OS ecosystem which we&rsquo;re just entering.
          Secondly, it&rsquo;s discouraged for a reason - it really does drain
          phone batteries to use Bluetooth regularly, and we&rsquo;re finding
          out how best to balance this with functionality.
        </p>

        <p><em>Are there any scenarios in which my data is vulnerable?</em></p>

        <p>
          It&rsquo;s safe to assume that yes, a sufficiently dedicated and
          well-resourced actor can probably use generic smartphone data
          maliciously. For example, by accessing the phones of many users, an
          actor could find matching random numbers to work out who&rsquo;s been
          in proximity to who. At that point, though, it would be far easier and
          more lucrative for any such actor to exploit a range of other apps
          installed on the average phone, or the hardware of the device itself.
          We&rsquo;re confident that our app doesn&rsquo;t add any more to the
          minor threat of hacking someone already faces simply by owning a
          smartphone.
        </p>

        <p><em>Why are you so obsessed with privacy?</em></p>

        <p>
          The answer is two-fold.<br />
          Firstly, we believe that in the wrong hands, information can be
          dangerous. Specific location data can reveal your schedule, routine,
          and hobbies. A close analysis can reveal where you eat, sleep, work,
          and worship; it implies who you spend time with, as well as where and
          when that time is spent. We believe that you should have maximal
          control of this information, and with the development of
          <a
            href="https://www.businessinsider.com/alibaba-coronavirus-chinese-app-quarantine-color-code-2020-3"
            >similar but more invasive</a
          >
          programs to combat the spread of COVID-19 we worry that this belief
          may fall by the wayside. We don&rsquo;t want anyone to have to choose
          between their safety and their privacy; we believe that we can provide
          both.<br />
          Secondly, regardless of what we believe, many potential users care
          about privacy - particularly in states which have less rigorous legal
          frameworks around human rights. We want these individuals to feel
          safe. We want these individuals to be safe, from threats both as big
          as national governments and as small as a virus. A privacy-centred app
          is one that they can feel comfortable using - and the more users we
          have, the better off everybody is.
        </p>

