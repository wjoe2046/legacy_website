        <h3>Why we&rsquo;re integrating Bluetooth</h3>

        <p>
          There are a few advantages Bluetooth has over GPS. It&rsquo;s blocked
          by walls, therefore more accurately reflecting functional proximity:
          for example, two people in the same room would be seen as at higher
          infection risk than in neighbouring rooms, while GPS would simply
          record them as being the same distance apart. It works in places where
