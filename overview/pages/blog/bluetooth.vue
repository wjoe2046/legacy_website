          not giving anyone identifiable information - including us. Our
          anonymous Bluetooth database would only be interpretable by the
          individual phones that connect to it, and they would only be
          interpreting the lines of the database relevant to them - it would not
          be interpretable by a human being. This is great news in its own
          right, but what makes it particularly appealing is that this should
          allow us to guarantee that we comply with HIPAA.
        </p>

