          distinctive travel patterns - say, spending every Thursday in a small
          town in rural France and every second Friday in a different small town
          in rural Sweden - this may be insufficient, and we&rsquo;re seeking
          security researchers in the field of data reidentification to look
          over this problem. However, since our security plan doesn&rsquo;t
          associate contact events with individual users, this problem is mostly
          (and maybe entirely) solved under our current plan. But we&rsquo;re
          double-checking this.
